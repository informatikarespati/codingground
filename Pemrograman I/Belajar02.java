public class Belajar02 {
    public static void main(String[] args) {
        int panjang;
        int lebar;
        int luas;
        int keliling;
        
        panjang = 10;
        lebar = 15;
        
        luas = panjang * lebar;
        keliling = 2 * (panjang + lebar);
        
        System.out.println("Luas = " + luas);
        System.out.println("Keliling = " + keliling);
    }
}